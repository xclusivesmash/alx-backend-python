#!/usr/bin/env python3
"""
module: 1-concat
Description: concatenation of 2 strings.
"""


def concat(str1: str, str2: str) -> str:
    """Type annotations.
    Args:
        str1 (str): input one.
        str2 (str): input two.
    Return:
        combo (str): str1 + str2
    """
    return str1 + str2
